let puntaje=5;

// hay dos maneras de ir incrementando los numeros
console.log(puntaje);
// se muestre y despues se incremente
puntaje++; //5
console.log(puntaje);
// se incremente y despues se muestre
++puntaje;//6

// se muestre y despues se decremente
puntaje--;

// se decremente y despues se muestre
--puntaje;