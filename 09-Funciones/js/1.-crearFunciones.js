// las funciones nos permiten tener un codigo mas ordenado y mas simple de leer ademas de que sean reutilizables

// hay dos maneras de crear funciones en js

//declaracion de funciones

// lo que colocaremos dentro de los parentesis de la funcion son los parametros de la funcion
function sumar()
{
    console.log(2+2);
}

sumar();


// expresion de funcion
const sumar2= function()
{
    console.log(3+3);
}

sumar2();